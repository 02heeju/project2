package com.example.project2;

public class menu_list_item {

    String owner_name;
    String place;
    String number_of_people;
    String time;
    String comment;
    String member;

    public menu_list_item(String owner_name, String place, String number_of_people, String time, String comment, String member) {
        this.owner_name = owner_name;
        this.place = place;
        this.number_of_people = number_of_people;
        this.time = time;
        this.comment = comment;
        this.member = member;
    }
}
